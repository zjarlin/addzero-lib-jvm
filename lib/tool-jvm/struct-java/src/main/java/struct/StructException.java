package struct;

public class StructException extends Exception {
   public StructException() {
   }

   public StructException(String arg0) {
      super(arg0);
   }

   public StructException(Throwable arg0) {
      super(arg0);
   }

   public StructException(String arg0, Throwable arg1) {
      super(arg0, arg1);
   }
}
